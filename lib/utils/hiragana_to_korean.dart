  };

  static bool _isHiragana(String ch) {
    final code = ch.codeUnitAt(0);
    return code >= 0x3041 && code <= 0x3096;
  }

  static bool _isKatakana(String ch) {
    final code = ch.codeUnitAt(0);
    return code >= 0x30A1 && code <= 0x30F6;
  }

  static bool _isKana(String ch) => _isHiragana(ch) || _isKatakana(ch);

  static String convert(String reading) {
    if (reading.isEmpty) return '';

    final buffer = StringBuffer();
    int i = 0;

    while (i < reading.length) {
      final ch = reading[i];

      // っ / ッ (촉음 — 된소리)
      if (ch == 'っ' || ch == 'ッ') {
        if (i + 1 < reading.length) {
          // 복합음 뒤에 오는 경우 (っきゃ 등)
          if (i + 2 < reading.length) {
            final nextTwo = reading.substring(i + 1, i + 3);
            if (_compound.containsKey(nextTwo)) {
              buffer.write(_compound[nextTwo]!);
              i += 3;
              continue;
            }
          }
          final next = reading[i + 1];
          if (_double.containsKey(next)) {
            buffer.write(_double[next]!);
            i += 2;
            continue;
          } else if (_single.containsKey(next)) {
            buffer.write(_single[next]!);
            i += 2;
            continue;
          }
        }
        i++;
        continue;
      }

      // 복합음 (2글자) 처리
      if (i + 1 < reading.length) {
        final two = reading.substring(i, i + 2);
        if (_compound.containsKey(two)) {
          buffer.write(_compound[two]!);
          i += 2;
          continue;
        }
      }

      // 조사 「は」→ 와, 「へ」→ 에 (앞 글자가 가나일 때만)
      if (ch == 'は' && i > 0 && _isKana(reading[i - 1])) {
        buffer.write('와');
        i++;
        continue;
      }
      if (ch == 'へ' && i > 0 && _isKana(reading[i - 1])) {
        buffer.write('에');
        i++;
        continue;
      }

      // 단일 가나
      if (_single.containsKey(ch)) {
        buffer.write(_single[ch]!);
        i++;
        continue;
      }

      // 그 외 (공백, 숫자, 한자, 구두점 등)는 그대로
      buffer.write(ch);
      i++;
    }

    return buffer.toString();
  }
